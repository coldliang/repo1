package com.cqupt.model;


public class ItemsShowingModel{
	
	private String url;
	private boolean isFileReaded;
	private String originName;
	
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public boolean isFileReaded() {
		return isFileReaded;
	}
	public void setFileReaded(boolean isFileReaded) {
		this.isFileReaded = isFileReaded;
	}
	public String getOriginName() {
		return originName;
	}
	public void setOriginName(String originName) {
		this.originName = originName;
	}
	

}

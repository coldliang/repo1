package com.cqupt.model;

public class TestItem {
	private int testItemID;
	private int testID;
	private String testItemContent;
	private int testItemType;
	public int getTestItemID() {
		return testItemID;
	}
	public void setTestItemID(int testItemID) {
		this.testItemID = testItemID;
	}
	public int getTestID() {
		return testID;
	}
	public void setTestID(int testID) {
		this.testID = testID;
	}
	public String getTestItemContent() {
		return testItemContent;
	}
	public void setTestItemContent(String testItemContent) {
		this.testItemContent = testItemContent;
	}
	public int getTestItemType() {
		return testItemType;
	}
	public void setTestItemType(int testItemType) {
		this.testItemType = testItemType;
	}
	
	
}
